# Upgrade all available packages
winget upgrade --all

# Install a package
winget install <package>

# Remove a package
winget uninstall <package>

# Update software package
winget upgrade <package>

# List installed apps on Windows
winget list

# Display the package info
winget show <package>

# Manage repositories
winget source <options>

# Search for packages in repositories
winget search <search_string>

# Export the list of installed programs to a file
winget export